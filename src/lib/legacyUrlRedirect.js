/**
 * helper function to get the redirect URL for a given drupal ID.
 * @param {*} id - the drupal ID (e.g. 179)
 * @param {*} urls - the array of URLs (helpTextUrls or articleUrls)
 * @param {*} pathPrefix - the path prefix (hjaelp or artikel)
 * @returns the redirect URL (e.g. /hjaelp/soegning-baade-enkel-og-avanceret/yuwm5980txdpskxr6hbnes17)
 */
function getLegacyRedirect(id, urls, pathPrefix) {
  if (typeof id !== "string" && typeof id !== "number") {
    return null;
  }

  const drupalId = Number(id);

  if (!Number.isInteger(drupalId)) {
    return null;
  }

  const url = urls.find(({ drupalID }) => drupalID === drupalId);

  if (!url?.strapiId) {
    return null;
  }

  return `/${pathPrefix}/${url.encodedTitle}/${url.strapiId}`;
}

/**
 * Get new strapi url for a given help text drupalID.
 * @param {*} helpTextId - the help text drupalID (e.g. 179)
 * @returns the redirect URL (e.g. /hjaelp/soegning-baade-enkel-og-avanceret/yuwm5980txdpskxr6hbnes17)
 */
export function getStrapiHelpTextRedirect(helpTextId) {
  return getLegacyRedirect(helpTextId, helpTextUrls, "hjaelp");
}

/**
 * Get new strapi url for a given article drupalID.
 * @param {*} articleId - the article drupalID (e.g. 229)
 * @returns the redirect URL (e.g. /artikel/tag-toget-paa-ferie/w1n8qrsnj1xge50wlx5hf9yw)
 */
export function getStrapiArticleRedirect(articleId) {
  return getLegacyRedirect(articleId, articleUrls, "artikel");
}
